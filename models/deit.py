###### Following writen by Liu Xing
class DEIT(nn.Module):
    def __init__(self, pretrained=False, dim=128, num_classes_end=2):
        super(DEIT, self).__init__()
        self.encoder = deit_base_distilled_patch16_224(pretrained=pretrained)
        self.dropout = nn.Dropout(0.1)
        self.relu = nn.ReLU(inplace=True)
        self.linear = nn.Linear(1000, num_classes_end)

    def forward(self, x):
        embedding = self.encoder(x)
        x = self.dropout(embedding)
        x = self.linear(self.relu(x))
        return embedding, x

def deit(pretrained):
    net = DEIT(pretrained)
    return net
    


